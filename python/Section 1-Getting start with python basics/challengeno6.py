# # TODO challenge number 6, Go through mulitple list with one loop


keys=['name','lastname','age','dob']
values=['sri','ram',22,'03/09/2023',55]

for i,j in zip(keys,values):
    print(i,'is',j)
    
 #zip()=it is a build in function is used to combine the first iteration of the key list and second iteration of the value list    

# output:
# name is sri                                                    
# lastname is ram                                                
# age is 22
# dob is 03/09/2023